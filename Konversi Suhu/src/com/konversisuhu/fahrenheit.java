package com.konversisuhu;

public class fahrenheit {
    double toreamur() {
        return (konversisuhu.suhu - 32.0D) / 2.25D;
    }

    double tokelvin() {
        return (konversisuhu.suhu + 459.67D) / 1.8D;
    }

    double tocelcius() {
        return (konversisuhu.suhu - 32.0D) / 1.8D;
    }
}